package in.ts.icad.leagal.model;

public class RespondentAdvocate {
	
	private Integer respondentAdvId;
	private String respondentAdvName;
	
	private String respondentAddress;
	
	private Integer resAdvCode;

	public Integer getResAdvCode() {
		return resAdvCode;
	}

	public void setResAdvCode(Integer resAdvCode) {
		this.resAdvCode = resAdvCode;
	}

	public Integer getRespondentAdvId() {
		return respondentAdvId;
	}

	public void setRespondentAdvId(Integer respondentAdvId) {
		this.respondentAdvId = respondentAdvId;
	}

	public String getRespondentAdvName() {
		return respondentAdvName;
	}

	public void setRespondentAdvName(String respondentAdvName) {
		this.respondentAdvName = respondentAdvName;
	}

	public String getRespondentAddress() {
		return respondentAddress;
	}

	public void setRespondentAddress(String respondentAddress) {
		this.respondentAddress = respondentAddress;
	}
	

}
