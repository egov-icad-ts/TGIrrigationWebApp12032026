package in.ts.icad.leagal.model;

public class PetitionerAdvocate {
	
	private Integer petitionerAdvId;
	private String  petitionerAdvName;
	private String  petitionerAdvAddress;
	public Integer getPetitionerAdvId() {
		return petitionerAdvId;
	}
	public void setPetitionerAdvId(Integer petitionerAdvId) {
		this.petitionerAdvId = petitionerAdvId;
	}
	public String getPetitionerAdvName() {
		return petitionerAdvName;
	}
	public void setPetitionerAdvName(String petitionerAdvName) {
		this.petitionerAdvName = petitionerAdvName;
	}
	public String getPetitionerAdvAddress() {
		return petitionerAdvAddress;
	}
	public void setPetitionerAdvAddress(String petitionerAdvAddress) {
		this.petitionerAdvAddress = petitionerAdvAddress;
	}

}
