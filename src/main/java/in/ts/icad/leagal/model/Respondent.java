package in.ts.icad.leagal.model;

public class Respondent {
	
	private Integer respondentId;
	
	private String respondentName;
	
	
	private String respondentAddress;
	
	private Integer districtId;
	private Integer mandalId;
	private Integer villageId;
	
	private Integer unitId;
	private Integer circleId;
	private Integer divisionId;
	private Integer subdivisionId;


	public Integer getUnitId() {
		return unitId;
	}


	public void setUnitId(Integer unitId) {
		this.unitId = unitId;
	}


	public Integer getCircleId() {
		return circleId;
	}


	public void setCircleId(Integer circleId) {
		this.circleId = circleId;
	}


	public Integer getDivisionId() {
		return divisionId;
	}


	public void setDivisionId(Integer divisionId) {
		this.divisionId = divisionId;
	}


	public Integer getSubdivisionId() {
		return subdivisionId;
	}


	public void setSubdivisionId(Integer subdivisionId) {
		this.subdivisionId = subdivisionId;
	}


	public Integer getDistrictId() {
		return districtId;
	}


	public void setDistrictId(Integer districtId) {
		this.districtId = districtId;
	}


	public Integer getMandalId() {
		return mandalId;
	}


	public void setMandalId(Integer mandalId) {
		this.mandalId = mandalId;
	}


	public Integer getVillageId() {
		return villageId;
	}


	public void setVillageId(Integer villageId) {
		this.villageId = villageId;
	}


	public Integer getRespondentId() {
		return respondentId;
	}


	public void setRespondentId(Integer respondentId) {
		this.respondentId = respondentId;
	}


	public String getRespondentName() {
		return respondentName;
	}


	public void setRespondentName(String respondentName) {
		this.respondentName = respondentName;
	}


	public String getRespondentAddress() {
		return respondentAddress;
	}


	public void setRespondentAddress(String respondentAddress) {
		this.respondentAddress = respondentAddress;
	}
	
	

}
