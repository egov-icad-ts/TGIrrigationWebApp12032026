package in.ts.icad.leagal.model;

public class Circle {
	
private int circleId;
private int unitId;
private String circleName;
private Integer headquarterId;
private String status;

private String officeAdress;

private String email;

private Integer oldCircleId;
private Integer oldUnitId;

private String effectiveFrom;


public Integer getHeadquarterId() {
	return headquarterId;
}
public void setHeadquarterId(Integer headquarterId) {
	this.headquarterId = headquarterId;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
public String getOfficeAdress() {
	return officeAdress;
}
public void setOfficeAdress(String officeAdress) {
	this.officeAdress = officeAdress;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public Integer getOldCircleId() {
	return oldCircleId;
}
public void setOldCircleId(Integer oldCircleId) {
	this.oldCircleId = oldCircleId;
}
public Integer getOldUnitId() {
	return oldUnitId;
}
public void setOldUnitId(Integer oldUnitId) {
	this.oldUnitId = oldUnitId;
}
public String getEffectiveFrom() {
	return effectiveFrom;
}
public void setEffectiveFrom(String effectiveFrom) {
	this.effectiveFrom = effectiveFrom;
}
public int getCircleId() {
	return circleId;
}
public void setCircleId(int circleId) {
	this.circleId = circleId;
}
public String getCircleName() {
	return circleName;
}
public void setCircleName(String circleName) {
	this.circleName = circleName;
}
public int getUnitId() {
	return unitId;
}
public void setUnitId(int unitId) {
	this.unitId = unitId;
}

}
